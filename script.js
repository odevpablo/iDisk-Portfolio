

var btn = document.querySelector('#btsk');
var skillhtml = document.querySelector('.skillhtml');

btn.addEventListener('click', function () {
    if (skillhtml.getElementsByClassName.display === 'block') {
        skillhtml.style.display = 'none';
    } else {
        skillhtml.style.display = 'block';
    }
}) 

